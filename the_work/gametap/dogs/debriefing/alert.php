			<h2>Alert Level</h2>
			<h3>About the Alert System</h3>
			<p>The Gameland Security Alert System was created to inform the public of the current level of threat Gameland faces from space, monsters, lasers, and danger.  The system provides citizens with specific indication regarding certain sectors and the potential dangers they face.   The system combines information, assessments, and learnings and encapsulates them into an easy to read Alert Level.</p>
			<h3>Descriptions of Alerts</h3>
			<p>How to Use the Gameland Security Alert System</p>
			<ul>
				<li><strong>8-bit</strong> - Make a plan.  Create a space attack survival kit.  Be informed.  Watch the news a lot more.</li>
				<li><strong>16-bit</strong> - Make a check list.  Replace supplies that you aren't happy with.  Report all suspicious activities that may be related to space or monsters.</li>
				<li><strong>32-bit</strong> - Put a lot more things in your survival kit.  Make sure you have phone numbers for everyone.  Plan out routes to escape the horror and practice them.  Become even more suspicious.</li>
				<li><strong>64-bit</strong> - Don't take stupid chances by <a href="http://www.firstpersongetaways.com/">traveling</a>.  Review your plans.    Make sure your neighbors are not monsters.  Wait patiently for the alert to move up to 128.</li>
				<li><strong>128-bit</strong> - Listen to your local government officials.  Make sure your local government officials have not be replaced by space monsters.  Watch the TV.  Start using your emergency supplies.</li>
			</ul>
